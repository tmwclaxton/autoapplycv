    /** @var array<string, int> */
    private const FALLBACK = [
        'helvetica' => 556,
        'helvetica-bold' => 556,
        'times-roman' => 500,
        'times-bold' => 500,
    ];

    public static function normalizeKey(string $key): string
    {
        return match ($key) {
            'helvetica-bold', 'times-roman', 'times-bold' => $key,
            default => 'helvetica',
        };
    }

    public static function measureWidth(string $text, float $fontSize, string $metricsKey = 'helvetica'): float
    {
        $key = self::normalizeKey($metricsKey);
        $table = self::TABLES[$key];
        $fallback = self::FALLBACK[$key];
        $width = 0;
        $chars = preg_split('//u', $text, -1, PREG_SPLIT_NO_EMPTY) ?: [];

        foreach ($chars as $char) {
            $code = mb_ord($char, 'UTF-8');

            if ($code === false || $code > 255) {
                $width += $fallback;

                continue;
            }

            $width += $table[$code] ?? $fallback;
        }

        return ($width * $fontSize) / 1000;
    }

    public static function measureRenderedWidth(
        string $text,
        float $fontSize,
        string $metricsKey = 'helvetica',
        float $wordSpacing = 0.0,
    ): float {
        return self::measureWidth($text, $fontSize, $metricsKey) + (substr_count($text, ' ') * $wordSpacing);
    }

    /**
     * @return list<string>
     */
    public static function wrapToWidth(
        string $text,
        float $maxWidth,
        float $fontSize,
        string $metricsKey = 'helvetica',
    ): array {
        $widthLimit = max(1.0, $maxWidth);
        $lines = [];

        foreach (explode("\n", str_replace("\r\n", "\n", $text)) as $hardLine) {
            if ($hardLine === '') {
                $lines[] = '';

                continue;
            }

            if (self::measureWidth($hardLine, $fontSize, $metricsKey) <= $widthLimit) {
                $lines[] = $hardLine;

                continue;
            }

            $tokens = preg_split('/\s+/', $hardLine) ?: [];
            $tokens = array_values(array_filter($tokens, fn (string $token): bool => $token !== ''));
            $current = '';

            foreach ($tokens as $word) {
                $candidate = $current !== '' ? "{$current} {$word}" : $word;

                if (self::measureWidth($candidate, $fontSize, $metricsKey) <= $widthLimit) {
                    $current = $candidate;

                    continue;
                }

                if ($current !== '') {
                    $lines[] = $current;
                }

                if (self::measureWidth($word, $fontSize, $metricsKey) <= $widthLimit) {
                    $current = $word;

                    continue;
                }

                $chunk = '';
                $wordChars = preg_split('//u', $word, -1, PREG_SPLIT_NO_EMPTY) ?: [];

                foreach ($wordChars as $char) {
                    $next = $chunk.$char;

                    if ($chunk !== '' && self::measureWidth($next, $fontSize, $metricsKey) > $widthLimit) {
                        $lines[] = $chunk;
                        $chunk = $char;
                    } else {
                        $chunk = $next;
                    }
                }

                $current = $chunk;
            }

            if ($current !== '') {
                $lines[] = $current;
            }
        }

        return $lines !== [] ? $lines : [''];
    }
